// Создаем карту и устанавливаем её центр (координаты приблизительно посередине мира)
const map = L.map('map').setView([20, 0], 2);

// Добавляем тайлы (OpenStreetMap)
L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
  maxZoom: 19
}).addTo(map);

// Массив с локациями (название, координаты, твое сообщение)
const places = [
  { name: "Мекка", coords: [21.4225, 39.8262], message: "Мекка — священное место, которое мы обязательно посетим вместе!" },
  { name: "Нью-Йорк", coords: [40.7128, -74.0060], message: "Город мечты и огней — Нью-Йорк!" },
  { name: "Турция", coords: [39.9208, 32.8541], message: "Потрясающая Турция — нас ждет волшебство!" },
  { name: "Мальдивы", coords: [3.2028, 73.2207], message: "Мальдивы — место светящегося океана, где мы будем вместе." },
  { name: "Париж", coords: [48.8566, 2.3522], message: "Город романтики — Париж, с тобой это будет волшебно!" },
  { name: "Барселона", coords: [41.3851, 2.1734], message: "Барселона — место, которое пленяет сердце!" }
];

// Добавляем маркеры на карту
places.forEach(place => {
  L.marker(place.coords)
    .addTo(map)
    .bindPopup(`<b>${place.name}</b><br>${place.message}`);
});
